def run_agent3(features_csv:  str = FEATURES_CSV,
               agent0_json:   str = OUTPUT_JSON_A0,
               output_json:   str = OUTPUT_JSON) -> list:

    print("=" * 68)
    print("GigScore — Agent 3: Social Trust + Behavioral Scoring")
    print("Barclays Hack-O-Hire 2026 · Team GigScore · Plaksha University")
    print("=" * 68)

    # ── Load features ─────────────────────────────────────────────────────
    print(f"\n  Loading features from {features_csv}...")
    features_df = load_features(features_csv)
    print(f"  Loaded {len(features_df)} applicant(s), {features_df.shape[1]} features")

    # ── Load transactions for red flag detection ──────────────────────────
    transactions = []
    if Path(agent0_json).exists():
        with open(agent0_json) as f:
            a0 = json.load(f)
        transactions = a0.get('transactions', [])
        print(f"  Loaded {len(transactions):,} transactions for red flag detection")
    else:
        print(f"  ⚠ {agent0_json} not found — red flag detection limited")

    # ── Load Agent 2 default_prob ─────────────────────────────────────────
    # Agent 2 computes the real XGBoost default probability for each applicant.
    # We use it here for behavioral_safe_score. Falls back to 0.20 if missing.
    a2_result = {}
    a2_path   = Path(AGENT2_RESULT)
    if not a2_path.exists():
        # try generic pattern agent2_result_*.json
        matches = list(Path('.').glob('agent2_result_*.json'))
        if matches:
            a2_path = matches[0]
    if a2_path.exists():
        with open(a2_path) as f:
            a2_result = json.load(f)
        print(f"  Loaded Agent 2 result from {a2_path.name} "
              f"(default_prob={a2_result.get('default_prob', '?')})")
    else:
        print(f"  ⚠ Agent 2 result not found — using default_prob=0.20")
        print(f"    Run: python3 agents_012.py --mode score --skip_agent0")

    # ── Cohort baseline score (from master dataset) ───────────────────────
    COHORT_BASELINE = 3.55  # avg behavioral score across 22K+ gig workers

    results = []

    for _, row in features_df.iterrows():
        persona_id      = str(row.get('persona_id', 'unknown'))
        applicant_token = str(row.get('applicant_token', 'APPL_unknown'))
        monthly_income  = float(row.get('AMT_INCOME_MONTHLY', 20000))

        print(f"\n  Processing: {persona_id} ({applicant_token})")

        # Step 1: Behavioral score
        beh         = compute_behavioral_score(row)
        step3_score = beh['step3_behavioral_score']

        # Step 2: Red flags
        red_flags       = detect_red_flags(row, transactions)
        confirmed_count = sum(1 for f in red_flags if f['confirmed'])

        # Step 3: Score adjustment
        adj, delta = compute_score_adjustment(step3_score, COHORT_BASELINE, red_flags)

        # Step 4: Social trust score
        social_trust = compute_social_trust_score(row)

        # Step 5: Behavioral safe score — use Agent 2 default_prob if available
        model_default_prob = float(a2_result.get('default_prob', 0.20))
        behavioral_safe    = compute_behavioral_safe_score(model_default_prob, red_flags)

        # Zone recommendation
        zone = "CLEAR" if confirmed_count <= 1 else "REVIEW"

        # ── Build signal dict matching original JSON format ───────────────
        behavioral_signals = {
            "upi_success_rate":             float(row.get('upi_success_rate', 1.0)),
            "upi_inflow_outflow_ratio":     float(row.get('upi_inflow_outflow_ratio', 1.0)),
            "upi_merchant_diversity_score": float(row.get('upi_merchant_diversity_score', 0.5)),
            "income_regularity_cv":         float(row.get('upi_income_regularity_cv', 0.35)),
            "obligation_fulfillment_rate":  float(row.get('upi_obligation_fulfillment_rate', 0.5)),
            "spending_velocity":            float(row.get('upi_spending_velocity', 0.3)),
            "age":                          float(row.get('age', 32.0)),
            "years_employed":               float(row.get('years_employed', 1.5)),
            "employment_age_ratio":         float(row.get('employment_age_ratio', 0.05)),
            "monthly_income":               round(monthly_income, 2),
        }

        record = {
            "persona_id":               persona_id,
            "applicant_token":          applicant_token,
            "step3_behavioral_score":   step3_score,
            "behavioral_delta":         delta,
            "score_adjustment":         adj,
            "zone_recommendation":      zone,
            "model_default_probability":round(model_default_prob, 6),
            "behavioral_safe_score":    behavioral_safe,
            "portfolio_safe_rate":      PORTFOLIO_SAFE_RATE,
            "confirmed_red_flag_count": confirmed_count,
            "red_flag_details":         [
                {
                    "flag":      f["flag"],
                    "severity":  round(f["severity"], 4),
                    "confirmed": f["confirmed"],
                }
                for f in red_flags
            ],
            "behavioral_signals":       behavioral_signals,
            # ── Extra fields for Agent 4 ──────────────────────────────────
            "social_trust_score":       social_trust,
            "group_scores": {
                "group_a_social_trust":   beh["group_a_social_trust"],
                "group_b_behavioral_vel": beh["group_b_behavioral_vel"],
                "group_c_derived":        beh["group_c_derived"],
            },
        }

        results.append(record)

        # ── Print summary ─────────────────────────────────────────────────
        print(f"  {'─' * 50}")
        print(f"  Behavioral score    : {step3_score} / 10")
        print(f"  Behavioral delta    : +{delta:.4f} vs cohort")
        print(f"  Score adjustment    : +{adj} GigScore points → Agent 4")
        print(f"  Social trust score  : {social_trust} / 100")
        print(f"  Zone                : {zone}")
        print(f"  Confirmed red flags : {confirmed_count}")
        for f in red_flags:
            icon = "🔴" if f['confirmed'] else "🟡"
            print(f"    {icon} {f['flag']:<20} severity={f['severity']:.4f} | {f.get('detail','')}")

    # ── Save output JSON ──────────────────────────────────────────────────
    with open(output_json, 'w') as f:
        json.dump(results, f, indent=2, default=str)

    # ── Print final summary ───────────────────────────────────────────────
    print()
    print("╔══════════════════════════════════════════════════════════════════╗")
    print("║              AGENT 3 COMPLETE                                   ║")
    print("╠══════════════════════════════════════════════════════════════════╣")
    for r in results:
        adj_str = f"+{r['score_adjustment']}" if r['score_adjustment'] >= 0 else str(r['score_adjustment'])
        print(f"║  {r['persona_id']:<20} score={r['step3_behavioral_score']:<5} "
              f"adj={adj_str:<3} trust={r['social_trust_score']:<6} zone={r['zone_recommendation']:<7}║")
    print("╠══════════════════════════════════════════════════════════════════╣")
    print(f"║  📄 {output_json:<61}║")
    print("╠══════════════════════════════════════════════════════════════════╣")
    print("║  NEXT: Agent 4 — Final GigScore + Explanation                   ║")
    print("╚══════════════════════════════════════════════════════════════════╝")

    return results


