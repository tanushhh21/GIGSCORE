# STEP 3: RED FLAG DETECTION
# ─────────────────────────────────────────────────────────────────────────

def detect_red_flags(row: pd.Series, transactions: list) -> list:
    """
    Detect red flags from transaction narrations and feature values.

    Flags:
      gambling      — detected from transaction narrations
      rent          — absent rent payments in statement (inverted)
      spend_balance — total debits > total credits (net outflow)
    """
    flags = []

    # ── Gambling detection ────────────────────────────────────────────────
    gambling_pattern = re.compile(
        r"\bDREAM\s*11\b|\bRUMMY\b|\bBETTING\b|\bCASINO\b|\bMPL\b|\bWINZO\b|\bGAME\s*CASH\b",
        re.I
    )
    gambling_txns = [
        t for t in transactions
        if gambling_pattern.search(t.get('narration_clean', '') or t.get('narration', ''))
    ]
    gambling_confirmed = len(gambling_txns) >= 3  # 3+ gambling transactions = confirmed
    gambling_severity  = 0.6 if gambling_confirmed else 0.3
    flags.append({
        "flag":      "gambling",
        "severity":  gambling_severity,
        "confirmed": gambling_confirmed,
        "detail":    f"{len(gambling_txns)} gambling transactions detected" if gambling_txns
                     else "No gambling transactions found",
    })

    # ── Rent stress detection ─────────────────────────────────────────────
    rent_count    = int(row.get('rent_payment_count', 0))
    n_months      = max(row.get('upi_utility_payment_streak', 1), 1)
    # If rent count = 0 in a 12+ month statement → stress signal
    rent_absent   = rent_count == 0
    rent_confirmed= rent_absent  # confirmed if truly no rent payments
    rent_severity = 0.1211 if rent_confirmed else 0.0926
    flags.append({
        "flag":      "rent",
        "severity":  rent_severity,
        "confirmed": rent_confirmed,
        "detail":    f"No rent payments found in {n_months} months" if rent_absent
                     else f"Rent paid in {rent_count} transactions",
    })

    # ── Spend/balance detection ───────────────────────────────────────────
    # Total debits > total credits = net outflow = stress signal
    ior          = float(row.get('upi_inflow_outflow_ratio', 1.0))
    spend_stress = ior < 1.0   # spending more than earning
    flags.append({
        "flag":      "spend_balance",
        "severity":  0.6025,
        "confirmed": spend_stress,
        "detail":    f"Net outflow detected — inflow/outflow ratio = {ior:.4f}" if spend_stress
                     else f"Net saver — inflow/outflow ratio = {ior:.4f}",
    })

    return flags


# ─────────────────────────────────────────────────────────────────────────
# STEP 4: COMPUTE SCORE ADJUSTMENT FOR AGENT 4
# ─────────────────────────────────────────────────────────────────────────

def compute_score_adjustment(behavioral_score: float,
                              baseline_score: float,
                              red_flags: list) -> tuple:
    """
    Maps behavioral_delta to a GigScore adjustment (+0 to +5).
    Matches the logic in AGENT0-RESULT-final5.json exactly.

    Returns: (score_adjustment, behavioral_delta)
    """
    behavioral_delta = round(behavioral_score - baseline_score, 4)

    # Base adjustment from delta
    if behavioral_delta > 3.5:
        adj = 4
    elif behavioral_delta > 2.5:
        adj = 3
    elif behavioral_delta > 1.0:
        adj = 2
    elif behavioral_delta > 0.0:
        adj = 1
    else:
        adj = 0

    # Reduce by confirmed red flags (min 0)
    confirmed_flags = sum(1 for f in red_flags if f['confirmed'])
    adj = max(0, adj - confirmed_flags)

    return adj, behavioral_delta


# ─────────────────────────────────────────────────────────────────────────
# STEP 5: COMPUTE SOCIAL TRUST SCORE (0–100) for Agent 4 Layer 2
# ─────────────────────────────────────────────────────────────────────────

def compute_social_trust_score(row: pd.Series) -> float:
    """
    Social trust score for Agent 4's Layer 2 blend (10% weight).

    Based on:
      - Edge stability  : recurring payment ratio (pay same people monthly)
      - Node reliability: obligation fulfillment rate
      - Flow stability  : inflow/outflow ratio
      - Temporal depth  : utility payment streak
      - Network breadth : merchant diversity

    Returns: score 0–100
    """
    eps = 1e-6

    edge_stability  = float(row.get('upi_recurring_payment_ratio', 0.5))
    node_reliability= float(row.get('upi_obligation_fulfillment_rate', 0.5))
    ior             = float(row.get('upi_inflow_outflow_ratio', 1.0))
    flow_stability  = min(ior / 2.0, 1.0)
    diversity       = float(row.get('upi_merchant_diversity_score', 0.5))
    streak          = float(row.get('upi_utility_payment_streak', 0))
    temporal        = min(streak / 12.0, 1.0)

    # Brownie points for exceptional signals
    bonus = 0.0

    # Platform density bonus: 3000+ gig income transactions = embedded worker
    platform_txns = int(row.get('platform_income_txn_count', 0))
    if platform_txns >= 3000:
        bonus += 0.03
    elif platform_txns >= 1000:
        bonus += 0.01

    # Network density bonus: 30+ unique VPA counterparties
    unique_vpas = int(row.get('upi_unique_merchants', 0))
    if unique_vpas >= 30:
        bonus += 0.02

    raw_score = np.clip(
        0.25 * edge_stability
        + 0.30 * node_reliability
        + 0.20 * flow_stability
        + 0.15 * diversity
        + 0.10 * temporal
        + bonus,
        0, 1
    )

    return round(float(raw_score * 100), 2)


# ─────────────────────────────────────────────────────────────────────────
# STEP 6: COMPUTE BEHAVIORAL SAFE SCORE
# ─────────────────────────────────────────────────────────────────────────

def compute_behavioral_safe_score(model_default_prob: float,
                                   red_flags: list) -> float:
    """
    Behavioral safe score = P(non-default) adjusted for red flags.
    Matches AGENT0-RESULT-final5.json format.
    """
    base_safe = 1.0 - model_default_prob

    # Each confirmed red flag slightly reduces safe score
    for flag in red_flags:
        if flag['confirmed']:
            base_safe -= flag['severity'] * 0.05  # small reduction

    return round(float(np.clip(base_safe, 0, 1)), 6)


